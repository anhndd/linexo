package vn.edu.hcmut.linexo.domain.interactor.AI;

public class LineXOLocation {
    private int x;
    private int y;

    public LineXOLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
